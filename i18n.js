function setLanguage(lang) {
    if (!TRANSLATIONS[lang]) {
        console.warn(`ä¸æ”¯æ´çš„èªè¨€: ${lang}`);
        return;
    }
    localStorage.setItem('preferredLanguage', lang);
    document.documentElement.lang = lang;
    applyTranslations(lang);
}

