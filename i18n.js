// 取得當前語言（從 localStorage 或瀏覽器設定）
function getCurrentLanguage() {
    const saved = localStorage.getItem('preferredLanguage');
    if (saved && TRANSLATIONS[saved]) {
        return saved;
    }
    
    // 從瀏覽器語言設定判斷
    const browserLang = navigator.language || navigator.userLanguage;
    if (browserLang.startsWith('zh')) {
        return browserLang.includes('TW') || browserLang.includes('HK') ? 'zh-TW' : 'zh-CN';
    }
    if (browserLang.startsWith('ja')) return 'ja';
    if (browserLang.startsWith('ko')) return 'ko';
    if (browserLang.startsWith('en')) return 'en';
    
    return 'zh-TW'; // 預設繁體中文
}

// 設定語言
function setLanguage(lang) {
    if (!TRANSLATIONS[lang]) {
        console.warn(`不支援的語言: ${lang}`);
        return;
    }
    localStorage.setItem('preferredLanguage', lang);
    document.documentElement.lang = lang;
    applyTranslations(lang);
}

// 套用翻譯
function applyTranslations(lang) {
    const t = TRANSLATIONS[lang];
    if (!t) return;
    
    // 更新所有帶有 data-i18n 屬性的元素
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });
    
    // 更新所有帶有 data-i18n-placeholder 屬性的元素（專門用於 placeholder）
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });
    
    // 更新標題
    const titleEl = document.querySelector('title');
    if (titleEl) {
        titleEl.textContent = t.title;
    }
    
    // 觸發自訂事件，讓其他模組知道語言已變更
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

// 取得翻譯文字
function t(key, lang = null) {
    const currentLang = lang || getCurrentLanguage();
    const translation = TRANSLATIONS[currentLang];
    return translation && translation[key] ? translation[key] : key;
}

// 取得當前語言的完整翻譯物件
function getTranslation(lang = null) {
    const currentLang = lang || getCurrentLanguage();
    return TRANSLATIONS[currentLang] || TRANSLATIONS['zh-TW'];
}

// 初始化
function initI18n() {
    const lang = getCurrentLanguage();
    setLanguage(lang);
}

// 導出
if (typeof window !== 'undefined') {
    window.I18n = {
        getCurrentLanguage,
        setLanguage,
        applyTranslations,
        t,
        getTranslation,
        initI18n,
        SUPPORTED_LANGUAGES,
        TRANSLATIONS
    };
    
    // 頁面載入時初始化
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initI18n);
    } else {
        initI18n();
    }
}

